public class Switchcase {
    public static void main(String[] args) {
        String name = "Anay";
        switch (name) {
            case "Anay":
                System.out.println("Hello, Anay!");
                break;
            case "Harsh":
                System.out.println("Hi, Astra!");
                break;
            case "Arjun":
                System.out.println("Hey, Orion!");
                break;
            default:
                System.out.println("Name not found.");
        }
    }
}
